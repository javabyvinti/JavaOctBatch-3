package com.bmpl.javabasic;

public class DoWhile {

	public static void main(String[] args) {
		
		//do-while --> exit controlled loop
		
		//Menu-driven programs --> do-while
		
		// 1- add, 2--sub, 3-mul, 4-div, 5-exit
		
		int i = 11;
		
		do{
			System.out.println(i);
			i++; //11++
		}while(i!=5); //12<=10
		
	}

}
